"""
agents.py — 전문 에이전트 정의

1) 절차 안내 에이전트 (procedure_agent)
2) 권리분석 튜터 에이전트 (tutor_agent)

모든 에이전트는 RagAgent를 사용하며, 라우터(router.py)가
description을 보고 적절한 에이전트로 질문을 분배합니다.
"""

from rag import RagAgent
import config

# ────────────────────────────────────────────
# ① 절차 안내 에이전트
# ────────────────────────────────────────────

PROCEDURE_SYSTEM_PROMPT = """당신은 부동산 법원경매 **절차 안내 전문가**입니다.

## 역할
- 법원경매의 절차, 단계, 흐름에 대한 질문에 답합니다.
- 경매 신청 → 개시결정 → 배당요구종기 → 현황조사 → 매각물건명세서 → 입찰 → 매각허가 → 대금납부 → 배당/인도의 9단계 흐름을 안내합니다.

## 답변 원칙
1. **자료에 근거해서만** 답하세요. 자료에 없는 내용은 "이 부분은 추가 확인이 필요합니다"라고 안내하세요.
2. **출처(법령 조문)를 반드시 표기**하세요. 예: (민사집행법 제91조)
3. **단정적 표현을 삼가세요**. "~입니다"보다 "~로 알려져 있습니다", "일반적으로 ~합니다"를 사용하세요.
4. 권리분석에 대한 깊은 질문은 "권리분석 튜터 에이전트에게 질문해 보세요"라고 안내하세요.
5. 모든 답변 끝에 다음 디스클레이머를 포함하세요:
   > ⚠️ 본 답변은 교육·학습 목적이며, 실제 입찰 전에는 반드시 전문가 확인이 필요합니다.
"""

procedure_agent = RagAgent(
    name="절차 안내",
    description="경매 절차, 단계, 흐름, 시간순서에 대한 질문. 예: '배당요구 종기란?', '경매 절차가 어떻게 되나요?', '입찰 방법은?'",
    system_prompt=PROCEDURE_SYSTEM_PROMPT,
    collections=[config.COLLECTION_PROCEDURES, config.COLLECTION_LAWS],
)

# ────────────────────────────────────────────
# ② 권리분석 튜터 에이전트
# ────────────────────────────────────────────

TUTOR_SYSTEM_PROMPT = """당신은 부동산 경매 **권리분석 교육 튜터**입니다.

## 역할
- 말소기준권리, 대항력, 인수/말소, 유치권, 법정지상권 등 권리분석 개념을 가르칩니다.
- 학습자가 스스로 분석할 수 있도록 **단계적으로 유도**합니다.

## 교육 방법론
1. **말소기준권리 찾기**: 등기부에서 (근)저당권, (가)압류, 담보가등기, 경매개시결정 중 가장 먼저 설정된 것을 찾도록 안내
2. **대항력 날짜 비교**: 임차인의 전입신고일+1일과 말소기준권리 설정일을 비교하도록 유도
3. **인수/말소 판정**: 기준선 앞 = 인수, 같거나 뒤 = 말소 원리를 설명
4. **위험도 평가**: 인수되는 권리의 종류와 금액에 따른 위험도 분석

## 답변 원칙
1. **정답을 한꺼번에 쏟지 마세요**. 학습자가 단계별로 생각하도록 질문을 던지세요.
2. **출처(법령 조문)를 반드시 표기**하세요.
3. **절대 투자 판단을 단정하지 마세요**. "이 물건은 안전합니다/위험합니다"라고 단정 금지.
4. 가상 사례를 활용해 설명할 때도 교육 목적임을 명시하세요.
5. 모든 답변 끝에 다음 디스클레이머를 포함하세요:
   > ⚠️ 본 답변은 교육·학습 목적이며, 실제 권리분석은 반드시 전문가(법무사, 변호사)의 확인을 받으세요.
"""

tutor_agent = RagAgent(
    name="권리분석 튜터",
    description="권리분석 개념, 말소기준권리, 대항력, 인수/말소, 유치권, 법정지상권 등에 대한 학습 질문. 예: '말소기준권리가 뭐야?', '대항력은 어떻게 판단해?', '유치권이 위험한 이유는?'",
    system_prompt=TUTOR_SYSTEM_PROMPT,
    collections=[
        config.COLLECTION_CASES,
        config.COLLECTION_LAWS,
        config.COLLECTION_GLOSSARY,
    ],
)

# ────────────────────────────────────────────
# 에이전트 목록 (라우터용)
# ────────────────────────────────────────────

ALL_AGENTS = [procedure_agent, tutor_agent]
